package com.xt.java.admin;

public class AdminInfo {
	
	private String admin_ID;
	
	private String admin_Name;
	
	private String admin_Gender;
	
	private String admin_Age;
	
	private String admin_Level;
	
	private String admin_Password;
	
	public String getAdmin_Gender() {
		return admin_Gender;
	}

	public void setAdmin_Gender(String admin_Gender) {
		this.admin_Gender = admin_Gender;
	}

	public String getAdmin_Age() {
		return admin_Age;
	}

	public void setAdmin_Age(String admin_Age) {
		this.admin_Age = admin_Age;
	}

	public String getAdmin_ID() {
		return admin_ID;
	}

	public void setAdmin_ID(String admin_ID) {
		this.admin_ID = admin_ID;
	}

	public String getAdmin_Name() {
		return admin_Name;
	}

	public void setAdmin_Name(String admin_Name) {
		this.admin_Name = admin_Name;
	}

	public String getAdmin_Password() {
		return admin_Password;
	}

	public void setAdmin_Password(String admin_Password) {
		this.admin_Password = admin_Password;
	}

	public String getAdmin_Level() {
		return admin_Level;
	}

	public void setAdmin_Level(String admin_Level) {
		this.admin_Level = admin_Level;
	}
}
